    def gen_vc_register(self):
        register_addr_value = []

        #
        Dmem_base_addr = 0x0
        vc_size = 16 # unit: flit(32Bytes)

        #
        addr_offset = 0

        reg_vc_dmem_base_size = []

        for case in ['e2s', 'e2n', 'e2w', 'w2s', 'w2n', 'w2e',
                     'n2s', 'n2e', 'n2w', 's2e', 's2n', 's2w']:

            resp_dmem = reg_resp_vc_dmem()
            resp_dmem.resp_dmem_base = Dmem_base_addr + addr_offset
            resp_dmem.resp_dmem_size = vc_size
            addr_offset += vc_size

            rqst_dmem = reg_rqst_vc_dmem()
            rqst_dmem.rqst_dmem_base = Dmem_base_addr + addr_offset
            rqst_dmem.rqst_dmem_size = vc_size
            addr_offset += vc_size

            data_dmem = reg_data_vc_dmem()
            data_dmem.data_dmem_base = Dmem_base_addr + addr_offset
            data_dmem.data_dmem_size = vc_size
            addr_offset += vc_size

            reg_vc_dmem_base_size.append(resp_dmem)
            reg_vc_dmem_base_size.append(rqst_dmem)
            reg_vc_dmem_base_size.append(data_dmem)

        # n2self, s2self, w2self, e2self
        for _ in ['e2self', 'w2self', 'n2self', 's2self']:
            rqst_dmem = reg_rqst_vc_dmem()
            rqst_dmem.rqst_dmem_base = Dmem_base_addr + addr_offset
            rqst_dmem.rqst_dmem_size = vc_size
            addr_offset += vc_size
            reg_vc_dmem_base_size.append(rqst_dmem)
        #
        assert len(reg_vc_dmem_base_size) == 40
        for i in range(20):
            # addr
            bits_offset_0 = i
            reg_vc_dmem_addr_obj = reg_vc_addr(bits_offset_0 = bits_offset_0)
            reg_vc_dmem_addr = reg_vc_dmem_addr_obj.to_verilog_hex()
            reg_vc_dmem_ea = reg_vc_dmem_addr_obj.ea

            # value
            value1 = reg_vc_dmem_base_size[2*i].bits
            value2 = reg_vc_dmem_base_size[2*i+1].bits
            #
            reg_value = value1 * 2**(32) + value2
            reg_str = hex(reg_value)
            str_list = list(reg_str)
            while len(str_list) < 18:
                str_list.insert(2, '0')
            reg_str = ''.join(str_list)
