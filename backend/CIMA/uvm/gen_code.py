        thread_record_register_value[f'rec_cfg_merge_{rec_addr_offset}'] = rec_value.get_field_dict()

        # dmac_addr_offset = rec_addr_offset * 4
        dmac_addr_obj_0 = reg_dmac_cfg0_addr()
        dmac_addr_0 = dmac_addr_obj_0.to_verilog_hex()
        dmac_addr_obj_1 = reg_dmac_cfg1_addr()
        dmac_addr_1 = dmac_addr_obj_1.to_verilog_hex()
        dmac_addr_obj_2 = reg_dmac_cfg2_addr()
        dmac_addr_2 = dmac_addr_obj_2.to_verilog_hex()
        dmac_addr_obj_3 = reg_dmac_cfg3_addr()
        dmac_addr_3 = dmac_addr_obj_3.to_verilog_hex()
        dmac_addr_obj_4 = reg_dmac_cfg4_addr()
        dmac_addr_4 = dmac_addr_obj_4.to_verilog_hex()
        dmac_ea = dmac_addr_obj_0.ea
        dmac_value_0 = reg_dmac_cfg_0()
        dmac_value_1 = reg_dmac_cfg_1()
        dmac_value_2 = reg_dmac_cfg_2()
        dmac_value_3 = reg_dmac_cfg_3()
        dmac_value_4 = reg_dmac_cfg_4()

        dmac_value_0.dmact_dtid = dst_core_tid
        dmac_value_0.dmact_dcid = dst_core_id
        dmac_value_0.dmact_cutloc = 0
        dmac_value_0.dmact_sign = 1
        if d_type == '4bit':
            dmac_value_0.dmact_dst_dtype = 1
            dmac_value_0.dmact_src_dtype = 1
        elif d_type == '8bit':
            dmac_value_0.dmact_dst_dtype = 2
            dmac_value_0.dmact_src_dtype = 2
        dmac_value_0.dmact_bfr_size = buffer_size
        dmac_value_0.dmact_bfr_base = buffer_base

        dmac_value_1.dmact_dir = out_direction
        dmac_value_1.dmact_cout = fout_channel
        dmac_value_1.dmact_cin = fin_channel
        dmac_value_1.dmact_knl_size = k_size

        dmac_value_2.dmact_wout = fout_width
        dmac_value_2.dmact_win = fin_width
        dmac_value_2.dmact_hout = fout_height
        dmac_value_2.dmact_hin = fin_height
        dmac_value_2.dmact_stride = int(math.log(stride, 2))
        dmac_value_2.dmact_rdcid = rdc_pe_id
        dmac_value_2.dmact_daid = da_pe_id
        dmac_value_2.dmact_lut_type = activation_type
        # dmac_value_2.dmact_vcid = 0
        if (dst_core_name[4] == core_name[4] and  abs(int(dst_core_name[6]) - int(core_name[6])) == 1) or \
            (dst_core_name[6] == core_name[6] and  abs(int(dst_core_name[4]) - int(core_name[4])) == 1):
            dmac_value_2.dmact_vcid = 0
        else:
            dmac_value_2.dmact_vcid = 3

        dmac_value_3.dmact_qt_offset = 0
        dmac_value_3.dmact_qt_shift = 0
        dmac_value_3.dmact_qt_mtply = 1
        dmac_value_3.dmact_frm_num = thread_info['Conv_Struct']['batch']

        padding_remain = (fin_width + 2 * padding - k_size) % stride
        # dmac_value_3.dmact_pad_rt = padding
        dmac_value_3.dmact_pad_rt = padding
        dmac_value_3.dmact_pad_lt = max(0, padding - padding_remain)

        # dmac_value_3.dmact_pad_lo = max(0, padding - padding_remain)
        dmac_value_3.dmact_pad_lo = 0
        # dmac_value_3.dmact_pad_up = padding
        dmac_value_3.dmact_pad_up = 0

        # dmac_value_4
        assert fin_channel in [3, 4, 8, 16, 32, 64, 128, 256, 512], f'error dmac in channel: {fin_channel}'
        assert stride in [1, 2], f'error stride: {stride}'
        assert k_size in [1, 3, 5, 7, 9], f'error kernel size: {k_size}'
        dmac_value_4.correction_mode = 1 if fin_channel == 64 else 0
        dmac_value_4.mode_entry = 0 if fin_channel == 64 else 1
        dmac_value_4.stride_i = stride
        dmac_value_4.channel_num_i = fin_channel
        dmac_value_4.kernel_size_i = k_size
        assert d_type == '8bit', f'error data type {d_type}'
        dmac_value_4.data_type_i = 0 if d_type == '8bit' else 1
        dmac_value_4.tx_dir = out_direction
        dmac_value_4.shift_num_0 = dmac_sn_0
        dmac_value_4.shift_num_1 = dmac_sn_1

        register_addr_value.append((dmac_addr_0, dmac_ea, dmac_value_0.to_verilog_hex()))
        register_addr_value.append((dmac_addr_1, dmac_ea, dmac_value_1.to_verilog_hex()))
        register_addr_value.append((dmac_addr_2, dmac_ea, dmac_value_2.to_verilog_hex()))
        register_addr_value.append((dmac_addr_3, dmac_ea, dmac_value_3.to_verilog_hex()))
        register_addr_value.append((dmac_addr_4, dmac_ea, dmac_value_4.to_verilog_hex()))

        thread_record_register_value[f'thread_dmac_cfg0'] = dmac_value_0.get_field_dict()
        thread_record_register_value[f'thread_dmac_cfg1'] = dmac_value_1.get_field_dict()
        thread_record_register_value[f'thread_dmac_cfg2'] = dmac_value_2.get_field_dict()
        thread_record_register_value[f'thread_dmac_cfg3'] = dmac_value_3.get_field_dict()
        thread_record_register_value[f'thread_dmac_cfg4'] = dmac_value_4.get_field_dict()

        pull_addr_offset = rec_addr_offset

        pull_addr_obj = reg_pull_cfg_merge_addr(bits_offset_0=pull_addr_offset)
        pull_addr = pull_addr_obj.to_verilog_hex()
        pull_ea = pull_addr_obj.ea
        pull_value = reg_pull_cfg_merge()
        pull_value.dmem_size = buffer_size
        pull_value.rdc_num = max(0, rdc_num - 1)
        if d_type == '4bit':
            pull_value.flit_num = max(0, int(math.ceil(credit_value * fin_channel * (da_num+1) * 4 / 256)) - 1)
        elif d_type == '8bit':
            pull_value.flit_num = max(0, int(math.ceil(credit_value * fin_channel * (da_num+1) * 8 / 256)) - 1)

        # pull_value.flit_num = 0

        pull_value.da_num = max(0, da_num - 1)

        register_addr_value.append((pull_addr, pull_ea, pull_value.to_verilog_hex()))
        thread_record_register_value[f'pull_cfg_merge_{pull_addr_offset}'] = pull_value.get_field_dict()

        pull_appendix_addr_offset = rec_addr_offset * 64
        c = 0
        for src_id, src in thread_info['Src'].items():
            pull_appendix_addr_offset = pull_appendix_addr_offset + c
            assert pull_appendix_addr_offset < 1024
            bits_offset_0 = pull_appendix_addr_offset % 256
            bits_offset_1 = pull_appendix_addr_offset // 256
            pull_addr_obj = reg_pull_info_0_addr(bits_offset_0=bits_offset_0,
                                                    bits_offset_1=bits_offset_1)
